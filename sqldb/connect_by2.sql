-- 레벨 검색하기
-- EMPNO 1000번이 가장 상위
-- EMPNO 1000, 1001, 1002, 1003, 1004번의 관리자는 1000이다.

SELECT LEVEL, EMPNO, MGR  --MGR상위
FROM EMP
START WITH MGR IS NULL
CONNECT BY PRIOR EMPNO = MGR;  -- 조인조건


-- 최대 계층 수 구하기
SELECT MAX(LEVEL)
FROM EMP
START WITH MGR IS NULL
CONNECT BY PRIOR EMPNO = MGR;















